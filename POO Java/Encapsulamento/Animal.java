package Encapsulamento;

public class Animal {
	String nome, cor, raca, rg, tutor, mesVersario;
	int idade;
	public Animal() {
		
	}
	
	public Animal(String nome, String cor, String raca, int idade, String rg, String tutor, String mesVersario) {
		this.nome = nome;
		this.cor = cor;
		this.raca = raca;
		this.idade = idade;
		this.rg = rg;
		this.tutor = tutor;
		this.mesVersario = tutor;
		
	}
}
